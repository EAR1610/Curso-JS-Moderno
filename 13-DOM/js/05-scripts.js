const card = document.querySelectorAll('.card');
console.log(card);


//Si un elemento no existe, retorna un array vacio
const noExiste = document.querySelectorAll('no-existe');
console.log(noExiste);



/**
 * Important, diferencia entre querySelector y querySelectorAll
 * querySelector retorna el primero valor
 * querySelectorAll retorna todos los valores
 */