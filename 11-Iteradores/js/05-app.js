let i = 1000;

do {
    console.log(`Número ${i}`);
    i++;    //Incremento
} while (i<10);    //Condición

/**
 * Diferencia entre Do while, while y for Loop
 * La diferencia es que por lo menos el Do While se va a repetir 1 vez, aunque la condición no sea verdadera, 
 * mientras que los otros 2 bucles, la condición deberá de ser verdadera para que se ejecuten
 */